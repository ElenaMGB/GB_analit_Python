# Задание 3. Взаимосвязь между зарплатой и бонусами
# Создайте точечный график, где по оси X будет отображаться зарплата (salary), а по
# оси Y — бонусы (bonus). Размер точек на графике должен быть пропорционален
# количеству лет в компании (years_at_company). Этот график позволит исследовать
# взаимосвязь между зарплатой и бонусами и оценить влияние стажа на размер
# бонусов.
# Подсказка № 1
# Проверьте, что в столбцах salary, bonus и years_at_company отсутствуют
# пропущенные значения. Пропуски могут негативно повлиять на визуализацию.
# Используйте метод df.isnull().sum() для проверки наличия пропусков в столбцах
# и метод df.dropna() для их удаления, если необходимо.
# Подсказка № 2
# При создании точечного графика с sns.scatterplot(), используйте параметр size
# для изменения размера точек в зависимости от количества лет в компании
# (years_at_company). Это поможет визуально представить, как стаж влияет на размер
# бонусов.
# Подсказка № 3
# Проверьте, что размер точек на графике не слишком маленький или большой. Вы
# можете настроить размер точек с помощью параметра sizes в функции
# sns.scatterplot(), чтобы они были более читаемыми.
# Подсказка № 4
# Добавьте заголовок, метки осей и настройте стиль графика. Используйте функции
# plt.title(), plt.xlabel(), и plt.ylabel() для улучшения визуального
# представления графика и легкости интерпретации


import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

# Загрузка данных из CSV-файла
df = pd.read_csv('data.csv')

# Создание точечного графика с использованием Seaborn
# Ось X: зарплата (salary)
# Ось Y: бонусы (bonus)
# Размер точек пропорционален количеству лет в компании (years_at_company)
sns.scatterplot(x='salary', y='bonus', size='years_at_company', data=df)

# Настройка заголовка графика
plt.title('Взаимосвязь между зарплатой, бонусами и стажем')

# Настройка меток осей
plt.xlabel('Зарплата')
plt.ylabel('Бонусы')

# Отображение графика
plt.show()